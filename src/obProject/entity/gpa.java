package obProject.entity;

public class gpa {
        float gpa;
        float credit;

    public gpa(float gpa, float credit) {
        this.gpa = gpa;
        this.credit = credit;
    }

    public float getGpa() {
        return gpa;
    }

    public void setGpa(float gpa) {
        this.gpa = gpa;
    }

    public float getCredit() {
        return credit;
    }

    public void setCredit(float credit) {
        this.credit = credit;
    }
}
